    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        
        String name = nameinfo.getText().trim();
        String address =addressinfo.getText().trim();
        String phoneno = phonenoinfo.getText().trim();
        String department = cmbDept.getSelectedItem().toString();
       // int id = txtid.getText().charAt(0);
       
        if(name.isEmpty()|| address.isEmpty()|| phoneno.isEmpty() || department.isEmpty() ){
            JOptionPane.showMessageDialog(this,"All fields are required");
                }
        else{
            JOptionPane.showMessageDialog(this, "Infromation Entered");
           // JOptionPane.showMessageDialog(this, "Signup Error!");
           
       }
        try {
          
       Connection con = DBConnection.getConnection();

       String sql = "INSERT INTO  studentdata ( name, address, phoneno, department) VALUES(?,?,?,?)";
       PreparedStatement ps = con.prepareStatement(sql);

// Match the order of the columns defined in the SQL string above
//ps.setInt(1, id);            // id is 1st
       ps.setString(1, name);       // name is 2nd
       ps.setString(2, address);    // address is 3rd
       ps.setString(3, phoneno);    // phoneno is 4th
       ps.setString(4, department); // department is 5th

       ps.executeUpdate();

       JOptionPane.showMessageDialog(this, "Insert Successful!");
      //  new demologin().setVisible(true);
        //this.dispose();
        nameinfo.setText(" ");
        addressinfo.setText(" ");
        phonenoinfo.setText(" ");
        cmbDept.setSelectedIndex(0);
        //txtid.setText(" ");
        

        }catch (Exception e) {
            JOptionPane.showMessageDialog(this," ");
            System.out.println(e);
        }
    }//GEN-LAST:event_btnInsertActionPerformed

