    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:try {
        String name = nameinfo.getText().trim();
        String address =addressinfo.getText().trim();
        String phoneno = phonenoinfo.getText().trim();
        String department = cmbDept.getSelectedItem().toString();

        try{ 
        Connection con = DBConnection.getConnection();

        String sql = "Update studentdata SET name =?,address=?,phoneno=?,department=? WHERE id =?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1,nameinfo.getText());
        ps.setString(2,addressinfo.getText());
        ps.setString(3,phonenoinfo.getText());
        ps.setString(4,cmbDept.getSelectedItem().toString());
       // ps.setInt(5,Integer.parseInt(txtid.getText()));
        
        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Update Successful!");
        ps.close();
        con.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

